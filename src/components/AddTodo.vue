<template>
    <div>
        <form @submit="addTodo">
            <input type="text" v-model="title" name="title" placeholder="Add Todo ...." autocomplete="off">
            
            <input type="submit" value="Submit" class="btn">
             </form>
            
        </div>

</template>

<script>
export default {
 name : "AddTodo",
 data(){
     return{
         title: ''
     }
 },
 methods: {
     addTodo(e){
e.preventDefault();
const newTodo = {
    id: 4,
    title : this.title,
    completed: false

}
//Send via emit
this.$emit('add-todo', newTodo);
this.title = '';
     }
 }
}
</script>


